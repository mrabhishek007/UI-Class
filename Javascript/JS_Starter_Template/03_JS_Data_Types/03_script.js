// number example
var jsVersion = 5 ;
console.log( 'JsVersion : ' + jsVersion +  ',data type : ' + typeof jsVersion );

// String Examples
var appName = "JS App" ;
console.log(  'value : ' + appName + ', data type : ' + typeof appName);

// boolean Examples
var isJsEasy = true;
console.log('value : ' + isJsEasy + ',data type : ' + typeof isJsEasy);

// reassignment example of variables
var abc ;
console.log('value : ' + abc + ',data type : ' + typeof abc);

abc = 20; ;
console.log('value : ' + abc + ',data type : ' + typeof abc);

abc = 'test';
console.log('value : ' + abc + ',data type : ' + typeof abc);

abc = true;
console.log('value : ' + abc + ',data type : ' + typeof abc);

abc = null;
console.log('value : ' + abc + ',data type : ' + typeof abc);
